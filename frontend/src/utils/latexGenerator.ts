function buildPublicationsSection(publications: string[]): string {
  let section = `%-------------------------------------------------------------------------------
% PUBLICATIONS
%-------------------------------------------------------------------------------
\\cvsection{Publications}

\\begin{cventries}
`

  publications.forEach(pub => {
    section += `  \\cventry
    {${escapeLatex(pub)}}
    {}{}{}{}
`
  })

  section += `\\end{cventries}
`

  return section
}
